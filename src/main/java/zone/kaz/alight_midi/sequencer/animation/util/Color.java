package zone.kaz.alight_midi.sequencer.animation.util;

public class Color {

    int r = 0;
    int g = 0;
    int b = 0;

    public Color(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public int getR() {
        return r;
    }

    public int getB() {
        return b;
    }

    public int getG() {
        return g;
    }

}
